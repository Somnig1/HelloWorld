package oop;

public class Calculator {
    public void addition(double tal1, double tal2) {

        double summa = tal1 + tal2;
        System.out.println(summa);


    }

    public void subtraction(double tal1, double tal2) {

        double summa = tal1 - tal2;
        System.out.println(summa);


    }

    public void division(double tal1, double tal2) {

        double summa = tal1 / tal2;
        System.out.println(summa);


    }

    public void multiplikation(double tal1, double tal2) {

        double summa = tal1 * tal2;
        System.out.println(summa);


    }

    public void modulus(double tal1, double tal2) {

        double summa = tal1 % tal2;
        System.out.println(summa);

    }
}